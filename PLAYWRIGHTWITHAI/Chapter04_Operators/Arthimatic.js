let a = 10, b = 3;
a + b   // 13 (Addition)
a - b   // 7  (Subtraction)
a * b   // 30 (Multiplication)
a / b   // 3.33 (Division)
a % b   // 1  (Modulus - remainder)
a ** b  // 1000 (Exponentiation)
a++     // 11 (Increment)
b--     // 2  (Decrement)


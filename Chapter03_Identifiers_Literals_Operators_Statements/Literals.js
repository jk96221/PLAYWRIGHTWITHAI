//What is Literal?
//Literal is a value that is assigned to a variable.
//Different Type of Literals
//1.String Literals
//2.Number Literals
//3.Boolean Literals
//4.Null Literals
//5.Undefined Literals
//6.Object Literals
//7.Array Literals
//8.Function Literals
//9.RegExp Literals
//10.Template Literals

//1.String Literals
let myString = "Hello World";
//This is a valid string literal
console.log(myString);

//2.Number Literals
let myNumber = 10;
//This is a valid number literal
console.log(myNumber);

//3.Boolean Literals
let myBoolean = true;
//This is a valid boolean literal
console.log(myBoolean);

//4.Null Literals
let myNull = null;
//This is a valid null literal
console.log(myNull);

//5.Undefined Literals
let myUndefined = undefined;
//This is a valid undefined literal
console.log(myUndefined);

//6.Object Literals
let myObject = {
    name: "John",
    age: 30,
    city: "New York"
};
//This is a valid object literal
console.log(myObject);

//7.Array Literals
let myArray = [1, 2, 3, 4, 5];
//This is a valid array literal
console.log(myArray);

//8.Function Literals
let myFunction = function () {
    console.log("Hello World");
};
//This is a valid function literal
console.log(myFunction);

//9.RegExp Literals
let myRegExp = /Hello World/;
//This is a valid regular expression literal
console.log(myRegExp);

//10.Template Literals
let myTemplate = `Hello World`;
//This is a valid template literal
console.log(myTemplate);

//literal Rules:
//1.String Literals cannot be empty
//2.Number Literals cannot be empty
//3.Boolean Literals cannot be empty
//4.Null Literals cannot be empty
//5.Undefined Literals cannot be empty
//6.Object Literals cannot be empty
//7.Array Literals cannot be empty
//8.Function Literals cannot be empty
//9.RegExp Literals cannot be empty
//10.Template Literals cannot be empty

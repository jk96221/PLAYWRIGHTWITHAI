//1.Arithmetic Operators
let myArithmetic = 10 + 20;
//This is a valid arithmetic operator
console.log(myArithmetic);  //30

//2.Assignment Operators
let myAssignment = 10;
//This is a valid assignment operator
console.log(myAssignment);  //10

//3.Comparison Operators
let myComparison = 10 == 20;
//This is a valid comparison operator
console.log(myComparison);  //false

//4.Logical Operators
let myLogical = 10 && 20;
//This is a valid logical operator
console.log(myLogical);  //20

//5.Bitwise Operators
let myBitwise = 10 & 20;
//This is a valid bitwise operator
console.log(myBitwise);  //0

//6.Ternary Operators
let myTernary = 10 ? 20 : 30;
//This is a valid ternary operator
console.log(myTernary);  //20

//7.Unary Operators
let myUnary = 10++;
//This is a valid unary operator
console.log(myUnary);  //10


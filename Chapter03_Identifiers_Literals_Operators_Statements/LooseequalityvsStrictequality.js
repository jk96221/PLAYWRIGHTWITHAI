//What is Loose equality?
//Loose equality is a comparison operator that checks if two values are equal, regardless of their data types.
//Syntax: ==
//Example: 
let myLooseEquality = 10 == "10";
//This is a valid loose equality operator
console.log(myLooseEquality);

//What is Strict equality?
//Strict equality is a comparison operator that checks if two values are equal and have the same data type.
//Syntax: ===
//Example: 
let myStrictEquality = 10 === "10";
//This is a valid strict equality operator
console.log(myStrictEquality);  
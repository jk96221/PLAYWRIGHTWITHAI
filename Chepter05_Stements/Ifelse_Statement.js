//If-Else Statement
//The if-else statement is a conditional statement that is used to execute a block of code if a certain condition is true, and another block of code if the condition is false.
//Syntax:
//if (condition) {
//    // code to be executed if condition is true
//}
//else {
//    // code to be executed if condition is false
//}
//Example:
let age = 20;
if (age >= 18) {
    console.log("You are eligible for voting");
}
else {
    console.log("You are not eligible for voting");
}

//Ternary Operator
//The ternary operator is a shorthand for the if-else statement.
//Syntax:
//condition ? expression1 : expression2
//Example:
let age1 = 20;
let result = age1 >= 18 ? "Eligible" : "Not Eligible";
console.log(result);


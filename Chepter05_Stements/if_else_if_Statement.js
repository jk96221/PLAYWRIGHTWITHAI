//if-else-if Statement
//The if-else-if statement is a conditional statement that is used to execute a block of code if a certain condition is true, and another block of code if the condition is false.
//Syntax:
//if (condition1) {
//    // code to be executed if condition1 is true
//}
//else if (condition2) {
//    // code to be executed if condition2 is true
//}
//else {
//    // code to be executed if condition1 and condition2 are false
//}
//Example:
let age1 = 20;
if (age1 >= 18) {
    console.log("You are eligible for voting");
}
else if (age1 >= 60) {
    console.log("You are eligible for senior citizen discount");
}
else {
    console.log("You are not eligible for voting");
}


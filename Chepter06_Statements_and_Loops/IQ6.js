let status_code=0;
switch(status_code){
    case false:
        console.log("false matched");
        break;
    case 0:
        console.log("0 matched");
        break;
    case null:
        console.log("null matched");
        break;
    case undefined:
        console.log("undefined matched");
        break;
    default:
        console.log("no match");
}  
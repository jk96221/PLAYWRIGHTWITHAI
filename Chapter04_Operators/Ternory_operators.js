//What is Ternory Operator?
//Ternory operator is a shorthand operator for if-else statement.
//Syntax: condition ? expression1 : expression2

let a = 10, b = 3;
console.log(a > b ? a : b);   // 10 (Ternory operator)   

let age=18;
console.log(age>=18?"Eligible for voting":"Not Eligible for voting");

let num=10;
console.log(num%2==0?"Even":"Odd");

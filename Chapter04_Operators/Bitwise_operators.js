//different types of bitwise operators
//AND (&), OR (|), XOR (^), NOT (~), Left Shift (<<), Right Shift (>>)

let a = 10, b = 3;
console.log(a & b);   // 2 (AND)
console.log(a | b);   // 11 (OR)
console.log(a ^ b);   // 9 (XOR)
console.log(~a);      // -11 (NOT)
console.log(a << b);  // 80 (Left Shift)
console.log(a >> b);  // 1 (Right Shift)    
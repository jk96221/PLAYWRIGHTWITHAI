//What is assignment operators?
//Assignment operators are used to assign values to variables.
//Different types of Assignment Operators

let x = 10;
console.log(x);

x += 5;
console.log(x);

x -= 5;
console.log(x);

x *= 5;
console.log(x);

x /= 5;
console.log(x);

x %= 5;
console.log(x);

x **= 5;
console.log(x);

x++;
console.log(x);

x--;
console.log(x);